
# TIPOS PRIMITIVOS:

# int = números inteiros
# float = números reais (Possuem ponto flutuante)
# bool = verdadeiro ou falso (True e False)
# str = Mensagens (necessitam de áspas)

x = int(input('Digite um número'))
y = int(input('Digite outro número'))
z = x + y

print('A soma entre {} e {} vale {}'.format(x, y, z))


