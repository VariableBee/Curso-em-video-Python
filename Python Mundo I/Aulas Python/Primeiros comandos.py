
# PRIMEIROS COMANDOS:

# Todas as funções necessitam de parêntese.
# Mensagens necessitam de '' (Delimitador de mensagens)

# Print é a função para mostrar algo na tela.
print('Olá mundo')
print(7+4)
print('7'+'4')
print('Olá', 5)

# VARIÁVEIS:

nome = 'Chris'
idade = '25'
peso = '92,6'

print(nome, idade, peso)

# Toda variável recebe um valor simbolizado por =
# PS: toda variável é um objeto em Python (Um objeto é mais que uma variável).

# Input é a função para inserir algum dado.
nome = input('Qual é seu nome?')  #
idade = input('Qual é sua idade?')
peso = input('Qual é seu peso?')

print(nome, idade, peso)